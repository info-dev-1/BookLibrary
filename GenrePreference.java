/**
 * This enum signals a person's preference of either fiction genre, non-fiction genre, or no genre. These values are used in computing statistics.
 * 
 * @author info-dev-1
 * @since 9/29/25
 */
public enum GenrePreference {
    FICTION,  // Value signaling the preference for the fiction genre of book.
    NON_FICTION,  // Value signaling the preference for the non-fiction genre of book.
    NO_GENRE  // Value signaling no preference of genres.
}
